a = 100
b = 300

#Arithmetic operations
print("# Arithmetic operators >")
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b) #modulo operator: gives the remainder after the division is complete

#Relational operators
print("# Relational operators >")
print(a>b)
print(a<b)
print(a == b)
print(a!=b)

#Logical operators
print("# Logical operators >")
print(a and b)
print(a or b)
print(not a)
print(True+True)

#Bitwise operators
print("# Bitwise operators >")
print(a&b)
print(a|b)

